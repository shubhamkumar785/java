public class CWH05operator {
    public static void main (String[] args){
        // arithmetic operator
        float a = 4.8f;
        float b = 1.1f;
        System.out.println(a%b);

        //asingment operator
        int c = 3;
        c *= 3;
        System.out.println(c);

        //comparison operator
        System.out.println(64 == 64);
        System.out.println(64 > 78);


        //logical operator
        System.out.println(64>5 && 64 < 8);
        System.out.println(64>5 || 64<5);


        // bitwise operator

    }
    
}
